"""
洛谷 P1058 - 立体图
题目：输出特定的图案

时间限制: 1000 ms
空间限制: 125 MB
难度: 普及
"""

n = int(input())

# 输出立体的正方形
for i in range(n):
    # 上面的斜线
    print(' ' * (n - i - 1) + '+' + '-' * (2 * i - 1) + '+')
    
    # 中间的竖线
    if i < n - 1:
        print(' ' * (n - i - 1) + '|' + ' ' * (2 * i + 1) + '|')

# 下面的斜线
for i in range(n - 1, -1, -1):
    print(' ' * (n - i - 1) + '+' + '-' * (2 * i - 1) + '+')
    
    if i > 0:
        print(' ' * (n - i - 1) + '|' + ' ' * (2 * i + 1) + '|')
